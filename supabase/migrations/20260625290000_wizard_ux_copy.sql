-- Wizard UX copy: plain Russian explanations and examples for non-technical users.

UPDATE emigro_wizard_questions
SET help_ru = CASE question_key
  WHEN 'passport_iso2' THEN 'Паспорт, с которым вы будете подавать заявление. Если паспортов несколько, выберите основной.'
  WHEN 'remote_income' THEN 'Например зарплата, ИП или фриланс от клиентов не в той стране, куда вы переезжаете. Это важно для виз цифрового кочевника.'
  WHEN 'monthly_income_eur' THEN 'Сумма после налогов в среднем за последние 3–6 месяцев. Пример: если получаете около €3 500 в месяц, введите 3500.'
  WHEN 'has_job_offer' THEN 'Оффер — это письменное предложение работы от работодателя в стране переезда. Нужно для рабочих виз и Blue Card: это карта ВНЖ для квалифицированной работы.'
  WHEN 'annual_salary_eur' THEN 'Брутто — сумма до налогов за год из оффера. Пример: €4 800 в месяц до налогов = примерно 57600.'
  WHEN 'passive_income_eur' THEN 'Пассивный доход — деньги, которые приходят без работы по найму: пенсия, аренда квартиры, дивиденды. Зарплату сюда не включайте.'
  WHEN 'savings_eur' THEN 'Деньги, которые можно показать выпиской из банка и реально использовать. Не считайте квартиру, машину или деньги, которые нельзя быстро снять.'
  WHEN 'willing_to_invest_eur' THEN 'Сколько вы готовы вложить именно в инвестиционный ВНЖ. Это не обычные сбережения и не D7. Если не планируете инвестировать, введите 0 или оставьте пустым.'
  WHEN 'has_university_degree' THEN 'Диплом бакалавра/магистра/специалиста, который можно подтвердить документами. Если не уверены, выберите «Нет» — это не заблокирует другие маршруты.'
  WHEN 'relocating_with_spouse' THEN 'Это влияет на сумму дохода/сбережений и пакет документов. Обычно супруг(а) идёт как член семьи или через воссоединение.'
  WHEN 'relocating_children_count' THEN 'Введите 0, если детей нет. Для детей обычно нужны дополнительные деньги, страховка, жильё и документы о родстве.'
  WHEN 'relocating_parents_count' THEN 'Введите 0, если никто. Взрослые родственники — сложный кейс: часто нужна отдельная проверка зависимости и права на воссоединение.'
  WHEN 'wants_study_route' THEN 'Это отдельный путь: вы поступаете в учебное заведение и показываете деньги на обучение и жизнь. Он не заменяет удалённую работу или пассивный доход.'
  WHEN 'has_university_admission' THEN 'Нужно письмо от университета, языковой школы или национальной системы поступления. Если вы только выбираете программу, ответьте «Нет».'
  WHEN 'study_budget_eur' THEN 'Деньги на обучение и проживание: свои сбережения, блокированный счёт или официальный спонсор. Пример: 12000.'
  WHEN 'can_show_study_funds' THEN 'Подтверждение — это выписка из банка, письмо спонсора, справки о доходе или другой документальный след денег.'
  ELSE help_ru
END
WHERE question_key IN (
  'passport_iso2',
  'remote_income',
  'monthly_income_eur',
  'has_job_offer',
  'annual_salary_eur',
  'passive_income_eur',
  'savings_eur',
  'willing_to_invest_eur',
  'has_university_degree',
  'relocating_with_spouse',
  'relocating_children_count',
  'relocating_parents_count',
  'wants_study_route',
  'has_university_admission',
  'study_budget_eur',
  'can_show_study_funds'
);

UPDATE emigro_wizard_questions
SET help_ru = 'Если там уже есть близкий родственник с гражданством или ВНЖ, это может быть маршрутом воссоединения. Это про вашу подачу к ним, а не про их переезд к вам.'
WHERE question_key LIKE 'has_family_in_%';
